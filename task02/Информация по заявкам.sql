SELECT c."фио"        AS "ФИО Клиента",
       r."описание"   AS "Описание от клиента",
       cat."название" AS "Категория",
       s."фио"        AS "ФИО Сотрудника"
FROM "заявки" r
         JOIN
     "категории" cat ON r."ид_категории" = cat."ид_категории"
         JOIN
     "клиент" c ON r."ид_клиента" = c."ид_клиента"
         LEFT JOIN
     "сотрудники" s ON r."ид_сотрудника" = s."ид_сотрудника"
ORDER BY r."ид_заявки";