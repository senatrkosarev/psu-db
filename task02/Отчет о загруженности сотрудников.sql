SELECT s."фио"                          AS "ФИО Сотрудника",
       COUNT(r."ид_заявки")             AS "Количество заявок",
       STRING_AGG(cat."название", '; ') AS "Категории заяврк"
FROM "заявки" r
         JOIN
     "сотрудники" s ON r."ид_сотрудника" = s."ид_сотрудника"
         JOIN
     "категории" cat ON r."ид_категории" = cat."ид_категории"
GROUP BY s."фио"
ORDER BY COUNT(r."ид_заявки") DESC;
